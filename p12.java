class Range
{
	public static void main(String args[])
	{
		byte p=24;
		short q=567;
		int r=567;
		long s=466789999L;
		long t=466789999;
		System.out.println("Byte p:="+p);
		System.out.println("Short q:="+q);
		System.out.println("Integer r:="+r);
		System.out.println("Long s:="+s);
		System.out.println("Long t:="+t);
	}
}